export function getAllRuPosts(): RuBlogPost[] {
  return [...RU_BLOG_POSTS].sort((a, b) => (a.date < b.date ? 1 : -1));
}

export function getRuPostBySlug(slug: string): RuBlogPost | undefined {
  return RU_BLOG_POSTS.find((p) => p.slug === slug);
}

export function getAllRuPostSlugs(): { slug: string }[] {
  return RU_BLOG_POSTS.map((p) => ({ slug: p.slug }));
}

export function getRuPostByHeSlug(heSlug: string): RuBlogPost | undefined {
  return RU_BLOG_POSTS.find((p) => p.heSlug === heSlug);
}

export function formatDateRu(iso: string): string {
  const [y, m, d] = iso.split('-').map(Number);
  const months = ['января','февраля','марта','апреля','мая','июня','июля','августа','сентября','октября','ноября','декабря'];
  return `${d} ${months[m - 1]} ${y}`;
}
